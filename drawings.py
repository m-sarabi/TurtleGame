def enhance_points(pts, offset=False, scale=1, flip=False, matrix=None):
    if matrix is not None:
        for pa in range(len(pts)):
            for p in range(len(pts[pa])):
                pts[pa][p][0] = matrix[pa][0] * pts[pa][p][0] + matrix[pa][2] * pts[pa][p][1] + matrix[pa][4]
                pts[pa][p][1] = matrix[pa][0] * pts[pa][p][1] + matrix[pa][3] * pts[pa][p][1] + matrix[pa][5]

    if flip:
        for pth in range(len(pts)):
            for point in range(len(pts[pth])):
                pts[pth][point][1] = pts[pth][point][1] * -1

    if scale != 1:
        for pth in range(len(pts)):
            for point in range(len(pts[pth])):
                pts[pth][point][0] = pts[pth][point][0] * scale
                pts[pth][point][1] = pts[pth][point][1] * scale

    if offset is True:
        all_points = []
        for p in pts:
            all_points += p
        min_x = min(all_points[_][0] for _ in range(len(all_points)))
        min_y = min(all_points[_][1] for _ in range(len(all_points)))
        max_x = max(all_points[_][0] for _ in range(len(all_points)))
        max_y = max(all_points[_][1] for _ in range(len(all_points)))

        for pth in range(len(pts)):
            for point in range(len(pts[pth])):
                pts[pth][point][0] = round(pts[pth][point][0] - (max_x + min_x) / 2, 2)
                pts[pth][point][1] = round(pts[pth][point][1] - (max_y + min_y) / 2, 2)

    return pts


def test_draw(pts, clrs, scale):
    shape_points = pts
    shape_colors = clrs

    shape_points = enhance_points(shape_points, False, scale, False)

    screen = turtle.Screen()
    screen.tracer(0, 0)

    apple_shape = turtle.Shape('compound')
    for i, path in enumerate(shape_points):
        points = path + [path[0]]
        apple_shape.addcomponent(points, shape_colors[i])
    screen.register_shape('apple', apple_shape)

    apple = turtle.Turtle()
    apple.penup()
    apple.shape('apple')
    apple.left(90)
    apple.shapesize(0.1, 0.1)
    apple.goto(0, 0)
    screen.update()
    print('done')
    turtle.done()


